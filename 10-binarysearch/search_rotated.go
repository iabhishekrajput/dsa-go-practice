package binarysearch

// SearchRotated finds target in a rotated sorted array and returns its index, or -1 if not found.
// Must run in O(log n) time. All values are unique.
func SearchRotated(nums []int, target int) int {
	// Your solution here
	left, right := 0, len(nums)-1

	for left <= right {
		mid := left + (right-left)/2

		if nums[mid] == target {
			return mid
		}

		if nums[left] <= nums[mid] {
			if nums[left] <= target && target < nums[mid] {
				right = mid - 1
			} else {
				left = mid + 1
			}
		} else {
			if nums[mid] < target && target <= nums[right] {
				left = mid + 1
			} else {
				right = mid - 1
			}
		}
	}

	return -1
}
